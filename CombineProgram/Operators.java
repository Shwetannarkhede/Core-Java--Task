package CombineProgram;

public class Operators {

	public static void main(String[] args) {
		byte a=4;
		byte b=2;
		
		// Arithmatic operator
		System.out.println("-----Arithmatic operator--------");
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		// Relational operator
		System.out.println("-----Relational operator--------");
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		
	

	}

}
